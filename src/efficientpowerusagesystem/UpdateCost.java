    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        try {
            Statement stmt=con.createStatement();
            String cst = jTextField1.getText();
            float cost = Float.parseFloat(cst);
            String lvl = (String) jComboBox1.getSelectedItem();
            int level = Integer.parseInt(lvl);
            if(cost>0){
            if(jComboBox3.getSelectedItem().equals("Hydro")){
                String sql = "update hydro_power set hp_cost="+cost+" where hp_level="+level;
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Update successful");
            }
            
            if(jComboBox3.getSelectedItem().equals("Thermal")){
                String sql = "update thermal_power set tp_cost="+cost+" where tp_level="+level;
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Update successful");
            }
            
            if(jComboBox3.getSelectedItem().equals("Renewable")){
                String sql = "update renewable_power set rp_cost="+cost+" where rp_level="+level;
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Update successful");
            }
            
            if(jComboBox3.getSelectedItem().equals("Nuclear")){
                String sql = "update nuclear_power set np_cost="+cost+" where np_level="+level;
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Update successful");
            }
            }
            else{
                JOptionPane.showMessageDialog(null, "Please enter a number >0 for input field 'Enter New Price'");
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter a number for input field 'Enter New Price'");
        }
        catch (SQLException ex) {
            Logger.getLogger(UpdateCost.class.getName()).log(Level.SEVERE, null, ex);
        }
        jComboBox1.setSelectedItem("1");
        jTextField1.setText("");
    }//GEN-LAST:event_jButton3MouseClicked

