    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        try {
            if(jLabel5.getText().equals("Too Short") || jLabel5.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Password is Too Short, please enter a Password with 8 or more characters");
            }
            else{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/efficientpowerusagesystem", "root", "Niranjana98");
            Statement stmt=con.createStatement();
            File myObj = new File("username.txt");
            Scanner myReader = new Scanner(myObj);
            String uname = myReader.nextLine();
            myReader.close();
            String sql = "Select password from user where username='"+uname+"' and password=?";
            pst = con.prepareStatement(sql);
            String pw = jPasswordField1.getText();
            PasswordHash ph = new PasswordHash();
            String pword = ph.getHash(pw.getBytes(), "SHA-1");
            pst.setString(1, pword);
            rs = pst.executeQuery();
            if(rs.next() && jPasswordField2.getText().equals(jPasswordField3.getText())){
                String npw = jPasswordField2.getText();
                String npword = ph.getHash(npw.getBytes(), "SHA-1");
                String sqlp = "update user set password='"+npword+"' where username='"+uname+"'";
                stmt.executeUpdate(sqlp);
                JOptionPane.showMessageDialog(null, "Password Changed Successfully");
                LoginPage lp = new LoginPage();
                lp.setVisible(true);
                lp.pack();
                lp.setLocationRelativeTo(null);
                lp.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
            }
            else if(!(jPasswordField2.getText().equals(jPasswordField3.getText()))){
                JOptionPane.showMessageDialog(null, "Fields 'Password' and 'Repeat Password' do not match");
            }
            else {
                JOptionPane.showMessageDialog(null, "Incorrect password for field 'Enter Current Password'");
            }
            }    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton5MouseClicked

