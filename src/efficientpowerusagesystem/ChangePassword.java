    private void jPasswordField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField2KeyReleased
        PasswordStrength ps = new PasswordStrength();
        String pw = jPasswordField2.getText();
        if(jPasswordField2.getText().length()==0){
            jLabel5.setText("");
        }
        else{
        if(ps.checkStr(pw).equals("Too Short")){
            jLabel5.setOpaque(true);
            jLabel5.setBackground(Color.red);
        }
        if(ps.checkStr(pw).equals("Weak")){
            jLabel5.setOpaque(true);
            jLabel5.setBackground(Color.orange);
        }
        if(ps.checkStr(pw).equals("Medium")){
            jLabel5.setOpaque(true);
            jLabel5.setBackground(Color.yellow);
        }
        if(ps.checkStr(pw).equals("Strong")){
            jLabel5.setOpaque(true);
            jLabel5.setBackground(Color.green);
        }
        jLabel5.setText(ps.checkStr(pw));
        } 
    }//GEN-LAST:event_jPasswordField2KeyReleased

